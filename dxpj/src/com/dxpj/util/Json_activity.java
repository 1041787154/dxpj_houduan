package com.dxpj.util;

public class Json_activity {
	
	private static String Activity_ID;
	private static String Activity_Name;
	private static String DID;
	private static String LinkPhone;
	private static String ParentID;
	private static String ResponsibleMan;

	public String getActivity_ID() {
		return Activity_ID;
	}
	public void setActivity_ID(String activity_ID) {
		Activity_ID = activity_ID;
	}
	public String getActivity_Name() {
		return Activity_Name;
	}
	public void setActivity_Name(String activity_Name) {
		Activity_Name = activity_Name;
	}
	public String getDID() {
		return DID;
	}
	public void setDID(String dID) {
		DID = dID;
	}
	public String getLinkPhone() {
		return LinkPhone;
	}
	public void setLinkPhone(String linkPhone) {
		LinkPhone = linkPhone;
	}
	public String getParentID() {
		return ParentID;
	}
	public void setParentID(String parentID) {
		ParentID = parentID;
	}
	public String getResponsibleMan() {
		return ResponsibleMan;
	}
	public void setResponsibleMan(String responsibleMan) {
		ResponsibleMan = responsibleMan;
	}
}
